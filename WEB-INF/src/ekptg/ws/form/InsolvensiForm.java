package ekptg.ws.form;

public class InsolvensiForm {
	
	String nama;
	String numIc;
	String tarikhSebutan;
	String tarikhPerintah;
	String cawangan;
	String noEstet;
	String catatan;
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getNumIc() {
		return numIc;
	}
	public void setNumIc(String numIc) {
		this.numIc = numIc;
	}
	public String getTarikhSebutan() {
		return tarikhSebutan;
	}
	public void setTarikhSebutan(String tarikhSebutan) {
		this.tarikhSebutan = tarikhSebutan;
	}
	public String getTarikhPerintah() {
		return tarikhPerintah;
	}
	public void setTarikhPerintah(String tarikhPerintah) {
		this.tarikhPerintah = tarikhPerintah;
	}
	public String getCawangan() {
		return cawangan;
	}
	public void setCawangan(String cawangan) {
		this.cawangan = cawangan;
	}
	public String getNoEstet() {
		return noEstet;
	}
	public void setNoEstet(String noEstet) {
		this.noEstet = noEstet;
	}
	public String getCatatan() {
		return catatan;
	}
	public void setCatatan(String catatan) {
		this.catatan = catatan;
	}
}
